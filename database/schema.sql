-- =====================================================================
--  LoanPro — PostgreSQL Database Schema
-- =====================================================================
--  Target  : PostgreSQL 15+
--  Purpose : Reference DDL for the LoanPro Loan Management System.
--
--  NOTE: At runtime the application uses Hibernate with
--        `spring.jpa.hibernate.ddl-auto=update`, so these tables are
--        created automatically from the JPA entities. This file is a
--        hand-maintained mirror of that generated schema, intended for
--        documentation, manual provisioning, and review.
--
--  Money columns use NUMERIC(15,2); interest rates use NUMERIC(5,2).
--  Enum-typed columns are stored as VARCHAR (Hibernate EnumType.STRING).
-- =====================================================================

-- Run order matters because of foreign keys. Drop in reverse order for a
